from panda3d.core import * 
from pandac.PandaModules import * #Load all PandaModules

# -------------------------------------------------------------------------------------
# -------------------------------------------------------------------------------------
# -------------------------------------------------------------------------------------

class VLC(object):
    '''
    you have to know vlc
    '''
    def __init__(self, ode_space):
        '''
        '''
        self.nodepath = loader.loadModel("data/models/vlc.egg")
        
        self.geom = OdeBoxGeom(ode_space, 6,6,20)
        self.geom.setCollideBits(0)
        self.geom.setCategoryBits(0)

    # -------------------------------------------------------------------------------------

    def setPosition(self, x, y, z):
        '''
        '''
        self.nodepath.setPos(x,y,z)
        self.geom.setPosition(x,y,z)
    
    # -------------------------------------------------------------------------------------
    
    def reparentTo(self, node):
        '''
        '''
        self.nodepath.reparentTo(node)
    
    # -------------------------------------------------------------------------------------
    
    def getGeom(self):
        '''
        '''
        return self.geom

